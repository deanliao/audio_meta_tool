def generate_beethoven_piano_sonatas_tracks(track_work):
    """
    Generates a dictionary mapping track numbers to title and movement information for Beethoven's piano sonatas.
    
    Note that it will populate all movements of a piano sonata piece to the corresponding track numbers.

    Args:
        track_work (list): A list of tuples. Each tuple: start track number and its corresponding no. of piano sonata.

    Returns:
        dict: A dictionary mapping track numbers to tuples of title and movement information.

    Example:
        >>> track_work = [(1, 6), (5, 24)]
        >>> generate_beethoven_piano_sonatas_tracks(track_work)
        {1: ('Piano Sonata No. 6 in F major, op. 10, No. 2', 'I. Allegro'),
         2: ('Piano Sonata No. 6 in F major, op. 10, No. 2', 'II. Allegretto'),
         3: ('Piano Sonata No. 6 in F major, op. 10, No. 2', 'III. Presto'),
         5: ('Piano Sonata No. 24 in F-sharp major, op. 78', 'I. Adagio cantabile - Allegro ma non troppo'),
         6: ('Piano Sonata No. 24 in F-sharp major, op. 78', 'II. Allegro vivace')
        }
    """
    track_title_mov = {}
    for track_no, work in track_work:
        title, movements = BEETHOVEN_PIANO_SONATAS[work]
        for i, movement in enumerate(movements):
            track_title_mov[track_no + i] = (title, movement)
    return track_title_mov

